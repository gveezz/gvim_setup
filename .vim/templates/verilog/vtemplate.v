////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Module I/O
////////////////////////////////////////////////////////////////////////////////

// System interface
// -- inputs
input SClk;    // COMMENT
input Reset_n; // COMMENT
input SyncRst; // COMMENT
// Data interface
// --inputs
input   DataInVal; // COMMENT
input  DataIn;     // COMMENT
// --outputs
output DataOutVal; // COMMENT
output DataOut;    // COMMENT

////////////////////////////////////////////////////////////////////////////////
// Internal parameters (localparams)
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Registered outputs
////////////////////////////////////////////////////////////////////////////////

reg DataOutVal;
reg DataOut;   

///////////////////////////////////////////////////////////////////////////////
// Virtually registered output (combinational regs)
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Internal registers
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Internal virtual registers (combinational regs)
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Internal wires
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Wires assignments
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Module description
////////////////////////////////////////////////////////////////////////////////

always @(posedge SClk or negedge Reset_n) begin
   if (!Reset_n) begin
      DataOutVal <= 0;
      DataOut    <= 0;
   end else begin
      if (SyncRst) begin
         DataOutVal <= 0;
         DataOut    <= 0;
      end
   end
end

endmodule 
