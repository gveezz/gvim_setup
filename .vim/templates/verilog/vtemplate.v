module $COMPONENT_NAME
(
   // System interface
   // -- inputs
   SClk,
   Reset_n,
   SyncRst,
   // Data interface
   // --inputs
   IDataVal,
   IData,
   // --outputs
   ODataVal,
   OData
);

////////////////////////////////////////////////////////////////////////////////
// External functions and tasks
////////////////////////////////////////////////////////////////////////////////
// Miscellaneous parameter task and functions

////////////////////////////////////////////////////////////////////////////////
// Configurable parameters
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Internal parameters (localparams) that affect ports
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Module I/O
////////////////////////////////////////////////////////////////////////////////

input  SClk;     // COMMENT
input  Reset_n;  // COMMENT
input  SyncRst;  // COMMENT
input  IDataVal; // COMMENT
input  IData;    // COMMENT
output ODataVal; // COMMENT
output OData;    // COMMENT

////////////////////////////////////////////////////////////////////////////////
// Internal parameters (localparams)
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Registered outputs
////////////////////////////////////////////////////////////////////////////////

reg ODataVal; // COMMENT
reg OData;    // COMMENT

///////////////////////////////////////////////////////////////////////////////
// Virtually registered output (combinational regs)
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Internal registers
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Internal virtual registers (combinational regs)
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Internal wires
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Wires assignments
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Module description
////////////////////////////////////////////////////////////////////////////////

always @(posedge SClk or negedge Reset_n) begin
   if (!Reset_n) begin
      ODataVal <= 0;
      OData    <= 0;
   end else begin
      if (SyncRst) begin
         ODataVal <= 0;
         OData    <= 0;
      end
   end
end

endmodule 
// end of $COMPONENT_NAME
